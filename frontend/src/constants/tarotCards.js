function buildMinorCard(suit, meta) {
  const suitInfo = suitTheme[suit];
  const cn = suitCN[suit];
  const name = `${cn}${meta.label}`;
  const nameEn = `${meta.labelEn} of ${suit === 'wands' ? 'Wands' : suit === 'cups' ? 'Cups' : suit === 'swords' ? 'Swords' : 'Pentacles'}`;
  const id = `${suit}-${String(meta.number).padStart(2, '0')}-${meta.rank}`;

  const detail = minorDetails[suit]?.[meta.rank];
  const upright = detail?.upright || { core: '', keywords: [], advice: '' };
  const reversed = detail?.reversed || { core: '', keywords: [], advice: '' };
  const symbols = Array.from(new Set([...(suitInfo?.symbols || []), ...((detail && detail.symbols) || [])]));
  const history = `${suitInfo?.history || ''}${detail?.history ? ` ${detail.history}` : ''}`.trim();
  const myth = detail?.myth || suitInfo?.myth || '';

  return {
    id,
    arcana: 'minor',
    name,
    nameEn,
    number: meta.number,
    suit,
    suitCN: cn,
    rank: meta.rank,
    rankLabel: meta.label,
    element: suitInfo?.element,
    theme: suitInfo?.theme,
    upright,
    reversed,
    symbols,
    history,
    myth
  };
}

const minorArcana = ['wands', 'cups', 'swords', 'pentacles'].flatMap((suit) =>
  rankMeta.map((meta) => buildMinorCard(suit, meta))
);

export const TAROT_CARDS = [...majorArcana, ...minorArcana];

